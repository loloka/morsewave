<?php
/**
 * Санитарная проверка значений ПЕРЕД публикацией в лидерборд (user_stats).
 *
 * Важно понимать границы: настоящий прогресс живёт в localStorage, сервер —
 * лишь слепок, поэтому «честного» эталона XP у нас нет. Это не пуленепробиваемая
 * защита, а разумный потолок, который отсекает грубую накрутку (999999999 в
 * консоли) и не трогает живых игроков. Реальный прогресс пользователя мы НЕ
 * меняем — только его публичную строку в таблице лидеров.
 *
 * ВАЖНО (v2.42, было багом в v2.40): потолок НЕ привязан к возрасту аккаунта.
 * Первая версия ограничивала XP/серию датой РЕГИСТРАЦИИ — казалось логичным
 * («серия не может быть длиннее аккаунта»), но это ложная посылка: прогресс
 * копится в localStorage с момента, когда человек НАЧАЛ тренироваться, а
 * аккаунт можно завести спустя недели после старта (архитектура сайта именно
 * такая, см. README — localStorage первичен, аккаунт опционален). В итоге
 * честного игрока со стажем 21 день, зарегистрировавшегося 10 дней назад,
 * обрезало до streak=10 — реальный кейс, поймали на живом лидерборде.
 * Поэтому здесь только ПЛОСКИЕ щедрые потолки, без привязки ко времени.
 */

// Абсолютный потолок XP: выше — заведомо инъекция (уровень ≈ 123 по формуле
// 1+√(xp/80) — ни один живой человек столько не наиграет). Щедро, но не
// бесконечно: 1.2 млн — это уже осмысленный предел, а не декорация.
const LB_XP_ABSOLUTE = 1200000;
// Абсолютный потолок серии дней: ~500 дней (полтора года подряд без единого
// пропуска) — заведомо инъекция, а не реальная серия.
const LB_STREAK_ABSOLUTE = 500;

/**
 * Возвращает [$xp, $streak], безопасные для публикации. Значения выше
 * абсолютного потолка обрезаются; факт обрезки пишется в error_log, чтобы
 * владелец мог заметить явных накрутчиков (grep по логу).
 */
function leaderboard_clamp($pdo, $userId, $xp, $streak) {
    $xp = max(0, (int) $xp);
    $streak = max(0, (int) $streak);

    $safeXp = min($xp, LB_XP_ABSOLUTE);
    $safeStreak = min($streak, LB_STREAK_ABSOLUTE);

    if ($safeXp < $xp || $safeStreak < $streak) {
        error_log(sprintf(
            '[leaderboard_guard] user #%d: xp %d->%d, streak %d->%d',
            (int) $userId, $xp, $safeXp, $streak, $safeStreak
        ));
    }

    return [$safeXp, $safeStreak];
}
