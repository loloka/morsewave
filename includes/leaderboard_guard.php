<?php
/**
 * Санитарная проверка значений ПЕРЕД публикацией в лидерборд (user_stats).
 *
 * Важно понимать границы: настоящий прогресс живёт в localStorage, сервер —
 * лишь слепок, поэтому «честного» эталона XP у нас нет. Это не пуленепробиваемая
 * защита, а разумный потолок, который отсекает грубую накрутку (999999 в
 * консоли) и не трогает живых игроков. Реальный XP пользователя мы НЕ меняем —
 * только его публичную строку в таблице лидеров.
 *
 * Почему модель привязана к ВОЗРАСТУ аккаунта, а не к времени между
 * синхронизациями: честная игра шлёт множество мелких обновлений подряд, и
 * лимит «XP в секунду/между пушами» рубил бы именно живых игроков (за 2 секунды
 * реально заработать 10 XP — мгновенный «темп» огромный). Возраст аккаунта
 * растёт медленно и честно ограничивает, сколько всего можно было заработать.
 */

// Щедрый потолок реального заработка за один день активной игры. Живой человек
// столько не набьёт (для ориентира: сессия ~30 групп по 5 символов ≈ 300 XP за
// ~15 минут; 25000 XP/день — это часы непрерывного гринда). Лучше не досмотреть,
// чем ошибочно срезать честного задрота.
const LB_XP_PER_DAY = 25000;
// Запас на самый первый день (аккаунту 0 полных дней от роду).
const LB_XP_BASELINE = 25000;
// Абсолютный потолок: выше — заведомо инъекция (уровень ≈ 112 по 1+√(xp/80)).
const LB_XP_ABSOLUTE = 1000000;

/**
 * Возвращает [$xp, $streak], безопасные для публикации. Значения выше
 * правдоподобного потолка обрезаются; факт заметной обрезки пишется в
 * error_log, чтобы владелец мог заметить накрутчиков (grep по логу).
 */
function leaderboard_clamp($pdo, $userId, $xp, $streak) {
    $xp = max(0, (int) $xp);
    $streak = max(0, (int) $streak);

    $stmt = $pdo->prepare('SELECT created_at FROM users WHERE id = :id');
    $stmt->execute(['id' => $userId]);
    $row = $stmt->fetch();
    $created = ($row && !empty($row['created_at'])) ? strtotime($row['created_at']) : time();
    $days = max(0, (int) floor((time() - $created) / 86400));

    // XP: базовый запас + суточный потолок за каждый прожитый день, но не выше
    // абсолютного предела.
    $maxXp = min(LB_XP_ABSOLUTE, LB_XP_BASELINE + LB_XP_PER_DAY * $days);
    // Серия дней физически не может быть длиннее возраста аккаунта (+1 на сегодня).
    $maxStreak = $days + 1;

    $safeXp = min($xp, $maxXp);
    $safeStreak = min($streak, $maxStreak);

    // Логируем только ЗАМЕТНУЮ обрезку (не мелкие округления), чтобы не засорять лог.
    if ($safeXp < $xp - 1000 || $safeStreak < $streak) {
        error_log(sprintf(
            '[leaderboard_guard] user #%d: xp %d->%d, streak %d->%d (age %d days)',
            (int) $userId, $xp, $safeXp, $streak, $safeStreak, $days
        ));
    }

    return [$safeXp, $safeStreak];
}
