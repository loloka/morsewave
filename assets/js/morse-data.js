/**
 * Множитель XP за скорость приёма. Быстрее принимать на слух объективно
 * тяжелее, поэтому за это доплачиваем — чтобы у новичка не было соблазна
 * подглядывать ради тех же 50 XP на неподъёмных 24 wpm (он теперь может
 * идти медленнее и всё равно набирать опыт, а скорость поощряется сверху).
 *
 * Пол = 1.0 на 12 wpm и НИЖЕ: медленный приём никогда не наказывается,
 * баланс существующих режимов на низких скоростях не меняется (важно —
 * полный алфавит в «Группах» на 12 wpm по-прежнему даёт ровно 2.0/символ).
 * Выше 12 wpm — линейная надбавка +4 % за каждый wpm, потолок 1.6 (≈27 wpm),
 * чтобы бонус не разгонялся бесконечно. Фармить нельзя: XP всё равно даётся
 * только за ВЕРНО принятый символ, а верно принять на высокой скорости
 * сложнее, а не легче.
 */
function speedXpFactor(wpm) {
    const w = Number(wpm) || 12;
    return Math.min(1.6, Math.max(1.0, 1 + (w - 12) * 0.04));
}
